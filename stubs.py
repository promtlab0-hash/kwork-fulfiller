    items = parse_brief_items(brief) or ["Как заказать?", "Сколько стоит?", "Какие сроки?"]
    sections = []
    for idx, item in enumerate(items):
        q = _name_of(item)
        if not q.endswith("?"):
            q = q + "?"
        topic = q.rstrip("?").lower()
        frame = _FAQ_FRAMES[idx % len(_FAQ_FRAMES)].format(topic=topic, topic_cap=topic.capitalize())
        tail = _FAQ_TAILS[idx % len(_FAQ_TAILS)]
        ans = f"{frame} {tail}"
        sections.append({"heading": q, "level": 2, "paragraphs": [ans]})
    return {"title": f"FAQ: {_business_line(brief)}", "sections": sections}


